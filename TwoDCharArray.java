package newtest;

public class TwoDCharArray {

	public static void main(String[] args) {
		
		char a[][] = new char [2][2];
		
		 a[0][0]='A';
		 a[0][1]='B';
		 a[1][0]='C';
		 a[1][1]='D';
		
		for(int i=0;i<2;i++) {
			
			for(int j=0;j<2;j++) {
				
				System.out.println(a[i][j]);
				
			}
			

	}

	}

}
